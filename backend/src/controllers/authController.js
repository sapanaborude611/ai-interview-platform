export const updateProfile = async (
  req,
  res
) => {
  try {
    const {
      name,
      targetRole,
      experience,
      skills,
      bio,
    } = req.body;

    const user = await User.findById(
      req.user._id
    );

    if (!user) {
      return res.status(404).json({
        success: false,
        message: "User not found",
      });
    }

    user.name = name || user.name;
    user.targetRole =
      targetRole || user.targetRole;
    user.experience =
      experience ?? user.experience;
    user.skills =
      skills || user.skills;
    user.bio = bio || user.bio;

    await user.save();

    res.status(200).json({
      success: true,
      user,
    });
  } catch (error) {
    res.status(500).json({
      success: false,
      message: error.message,
    });
  }
};